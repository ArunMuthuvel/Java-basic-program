public class Pattern1
{
            public static void main (String[]args)
                              {
                                int i=1;
                                int row=1;
                                while (row<=3)
                                     {
                                      int column=1;
                                       while (column<=4)
                                            {
                                             System.out.print(i+ "   ");
                                             i=i+1;
                                             column++;

                                             }
                                        System.out.println();

                                        row++;

                                      }
                                }
}
